

public class Jar{
  
 public static String itemName;
 public static int maxNumber;




  
  public Jar(String itemName, int maxNumber){
   
    this.itemName = itemName;
    this.maxNumber = maxNumber;
    
  
  }  
  public String getItemName(){
    return itemName;
  
  }
  public int getMaxNumber(){  
    return maxNumber;
  
  }

  

  


}